function parseRow(row) {
  // "12. Patrick Mahomes, KCQ" → "Patrick Mahomes"
  const head = row[0];
  const m = /^\d+\.\s+(.+?),\s+[A-Z]{2,4}Q?$/.exec(head);
  const name = m ? m[1] : head;
  const avg = parseFloat(row[row.length - 1]);
  return [name, avg];
}

